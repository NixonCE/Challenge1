public class monedas {


    public int argentina;
    public int boliviano;
    public int brasileño;
    public int chileno;
    public int colombiano;
    public int estadounidense;

    public monedas(int ARS, int BOB, int BRL, int CLP, int COP, int USD) {
        this.argentina = ARS;
        this.boliviano = BOB;
        this.brasileño = BRL;
        this.chileno = CLP;
        this.colombiano = COP;
        this.estadounidense = USD;
    }
}
